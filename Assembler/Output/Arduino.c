void AssemblyCodeLoader()
{
	// ;; BEGIN INT 00000001
	SetSRAM(0b0000000000000000, 0b11100001); // SET A, "0001"
	SetSRAM(0b0000000000000001, 0b11110000); // SET B, "0000"
	SetSRAM(0b0000000000000010, 0b10001111); // MOV8
	SetSRAM(0b0000000000000011, 0b11011110); // MOV_ALU_OUT XL
	SetSRAM(0b0000000000000100, 0b11010000); // INT
	// ;; END INT 00000001
	// ;; BEGIN MOV XL, 10000000b (MOV_CONST_BINARY)
	SetSRAM(0b0000000000000101, 0b11100000); // SET A, "0000"
	SetSRAM(0b0000000000000110, 0b11111000); // SET B, "1000"
	SetSRAM(0b0000000000000111, 0b10001111); // MOV8
	SetSRAM(0b0000000000001000, 0b11011110); // MOV_ALU_OUT XL
	// ;; END MOV XL, 10000000b (MOV_CONST_BINARY)
	// ;; BEGIN MOV XH, 0x00 (MOV_CONST_HEX1)
	SetSRAM(0b0000000000001001, 0b11100000); // SET A, "0000"
	SetSRAM(0b0000000000001010, 0b11110000); // SET B, "0000"
	SetSRAM(0b0000000000001011, 0b10001111); // MOV8
	SetSRAM(0b0000000000001100, 0b11011111); // MOV_ALU_OUT XH
	// ;; END MOV XH, 0x00 (MOV_CONST_HEX1)
	// ;; BEGIN MOV16 J, X (MOV16)
	SetSRAM(0b0000000000001101, 0b01010001); // MOV16 J, X
	// ;; END MOV16 J, X (MOV16)
	// ;; BEGIN MOV D, [J] (MOV_I_R8_16)
	SetSRAM(0b0000000000001110, 0b01000010); // MOV16 M, J
	SetSRAM(0b0000000000001111, 0b10010000); // LOAD D
	// ;; END MOV D, [J] (MOV_I_R8_16)
	// ;; BEGIN MOV XL, D (MOV_R8_R8)
	SetSRAM(0b0000000000010000, 0b00110000); // MOV XL, D
	// ;; END MOV XL, D (MOV_R8_R8)
	SetSRAM(0b0000000000010001, 0b11001110); // OUT C
	// ;; BEGIN MOV XL, 10000001b (MOV_CONST_BINARY)
	SetSRAM(0b0000000000010010, 0b11100001); // SET A, "0001"
	SetSRAM(0b0000000000010011, 0b11111000); // SET B, "1000"
	SetSRAM(0b0000000000010100, 0b10001111); // MOV8
	SetSRAM(0b0000000000010101, 0b11011110); // MOV_ALU_OUT XL
	// ;; END MOV XL, 10000001b (MOV_CONST_BINARY)
	// ;; BEGIN MOV XH, 0x00 (MOV_CONST_HEX1)
	SetSRAM(0b0000000000010110, 0b11100000); // SET A, "0000"
	SetSRAM(0b0000000000010111, 0b11110000); // SET B, "0000"
	SetSRAM(0b0000000000011000, 0b10001111); // MOV8
	SetSRAM(0b0000000000011001, 0b11011111); // MOV_ALU_OUT XH
	// ;; END MOV XH, 0x00 (MOV_CONST_HEX1)
	// ;; BEGIN MOV16 J, X (MOV16)
	SetSRAM(0b0000000000011010, 0b01010001); // MOV16 J, X
	// ;; END MOV16 J, X (MOV16)
	// ;; BEGIN MOV D, [J] (MOV_I_R8_16)
	SetSRAM(0b0000000000011011, 0b01000010); // MOV16 M, J
	SetSRAM(0b0000000000011100, 0b10010000); // LOAD D
	// ;; END MOV D, [J] (MOV_I_R8_16)
	// ;; BEGIN MOV XL, D (MOV_R8_R8)
	SetSRAM(0b0000000000011101, 0b00110000); // MOV XL, D
	// ;; END MOV XL, D (MOV_R8_R8)
	SetSRAM(0b0000000000011110, 0b11001110); // OUT C
	// ;; BEGIN MOV XL, 10000010b (MOV_CONST_BINARY)
	SetSRAM(0b0000000000011111, 0b11100010); // SET A, "0010"
	SetSRAM(0b0000000000100000, 0b11111000); // SET B, "1000"
	SetSRAM(0b0000000000100001, 0b10001111); // MOV8
	SetSRAM(0b0000000000100010, 0b11011110); // MOV_ALU_OUT XL
	// ;; END MOV XL, 10000010b (MOV_CONST_BINARY)
	// ;; BEGIN MOV XH, 0x00 (MOV_CONST_HEX1)
	SetSRAM(0b0000000000100011, 0b11100000); // SET A, "0000"
	SetSRAM(0b0000000000100100, 0b11110000); // SET B, "0000"
	SetSRAM(0b0000000000100101, 0b10001111); // MOV8
	SetSRAM(0b0000000000100110, 0b11011111); // MOV_ALU_OUT XH
	// ;; END MOV XH, 0x00 (MOV_CONST_HEX1)
	// ;; BEGIN MOV16 J, X (MOV16)
	SetSRAM(0b0000000000100111, 0b01010001); // MOV16 J, X
	// ;; END MOV16 J, X (MOV16)
	// ;; BEGIN MOV D, [J] (MOV_I_R8_16)
	SetSRAM(0b0000000000101000, 0b01000010); // MOV16 M, J
	SetSRAM(0b0000000000101001, 0b10010000); // LOAD D
	// ;; END MOV D, [J] (MOV_I_R8_16)
	// ;; BEGIN MOV XL, D (MOV_R8_R8)
	SetSRAM(0b0000000000101010, 0b00110000); // MOV XL, D
	// ;; END MOV XL, D (MOV_R8_R8)
	SetSRAM(0b0000000000101011, 0b11001110); // OUT C
	SetSRAM(0b0000000000101100, 0b11000011); // HLT
}
